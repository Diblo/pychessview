"""Theme name definitions for the pychessview package."""

from enum import StrEnum


class ThemeName(StrEnum):
    """Enumeration of built-in theme names.

    Attributes:
        DEFAULT: Built-in default theme name.
        STANDARD_CHESS: Built-in standard chess theme name.
    """

    DEFAULT = "default"
    STANDARD_CHESS = "default"
